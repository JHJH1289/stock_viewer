/* 9. Build STOCKS from existing trading data and legacy post symbols.
      For legacy posts, numeric six-digit symbols are treated as KRX; others as NASDAQ fallback. */
MERGE INTO STOCKS st
USING (
    SELECT DISTINCT MARKET_CODE, SYMBOL, STOCK_NAME
    FROM (
        SELECT MARKET_CODE, UPPER(SYMBOL) SYMBOL, STOCK_NAME
        FROM HOLDINGS
        WHERE SYMBOL IS NOT NULL

        UNION ALL

        SELECT MARKET_CODE, UPPER(SYMBOL) SYMBOL, STOCK_NAME
        FROM TRADE_ORDERS
        WHERE SYMBOL IS NOT NULL

        UNION ALL

        SELECT
            CASE
                WHEN REGEXP_LIKE(SYMBOL, '^[0-9]{6}$') THEN 'KRX'
                ELSE 'NASDAQ'
            END MARKET_CODE,
            UPPER(SYMBOL) SYMBOL,
            UPPER(SYMBOL) STOCK_NAME
        FROM POSTS_LEGACY
        WHERE SYMBOL IS NOT NULL
    )
) src
ON (st.MARKET_CODE = src.MARKET_CODE AND st.SYMBOL = src.SYMBOL)
WHEN NOT MATCHED THEN
    INSERT (MARKET_CODE, SYMBOL, STOCK_NAME)
    VALUES (src.MARKET_CODE, src.SYMBOL, src.STOCK_NAME);

/* 10. Create one STOCK board per known stock. */
INSERT INTO BOARDS (
    BOARD_SLUG,
    BOARD_NAME,
    BOARD_DESCRIPTION,
    BOARD_TYPE,
    MARKET_CODE,
    STOCK_ID,
    WRITE_ROLE,
    HIDDEN,
    SORT_ORDER
)
SELECT
    'stocks/' ||
        CASE WHEN st.MARKET_CODE = 'KRX' THEN 'KR' ELSE 'US' END ||
        '/' || st.SYMBOL,
    st.STOCK_NAME || ' 게시판',
    st.SYMBOL || ' 종목 게시판',
    'STOCK',
    CASE WHEN st.MARKET_CODE = 'KRX' THEN 'KR' ELSE 'US' END,
    st.STOCK_ID,
    'USER',
    0,
    100
FROM STOCKS st
WHERE NOT EXISTS (
    SELECT 1
    FROM BOARDS b
    WHERE b.BOARD_TYPE = 'STOCK'
      AND b.STOCK_ID = st.STOCK_ID
);

/* 11. Migrate legacy posts.
       - SYMBOL NULL -> free board.
       - SYMBOL set  -> generated stock board.
*/
INSERT INTO POSTS (
    POST_ID,
    BOARD_ID,
    USER_ID,
    TITLE,
    CONTENT,
    VIEW_COUNT,
    RECOMMEND_COUNT,
    CREATED_AT,
    UPDATED_AT
)
SELECT
    lp.POST_ID,
    COALESCE(stock_board.BOARD_ID, free_board.BOARD_ID),
    lp.USER_ID,
    lp.TITLE,
    lp.CONTENT,
    NVL(lp.VIEW_COUNT, 0),
    NVL(lp.RECOMMEND_COUNT, 0),
    NVL(lp.CREATED_AT, SYSTIMESTAMP),
    NVL(lp.UPDATED_AT, NVL(lp.CREATED_AT, SYSTIMESTAMP))
FROM POSTS_LEGACY lp
CROSS JOIN (
    SELECT BOARD_ID
    FROM BOARDS
    WHERE BOARD_SLUG = 'free'
) free_board
LEFT JOIN STOCKS st
    ON st.SYMBOL = UPPER(lp.SYMBOL)
   AND st.MARKET_CODE = CASE
        WHEN REGEXP_LIKE(lp.SYMBOL, '^[0-9]{6}$') THEN 'KRX'
        ELSE 'NASDAQ'
   END
LEFT JOIN BOARDS stock_board
    ON stock_board.BOARD_TYPE = 'STOCK'
   AND stock_board.STOCK_ID = st.STOCK_ID
WHERE NOT EXISTS (
    SELECT 1
    FROM POSTS p
    WHERE p.POST_ID = lp.POST_ID
);

/* 12. Migrate comments. */
INSERT INTO COMMENTS (
    COMMENT_ID,
    POST_ID,
    USER_ID,
    CONTENT,
    CREATED_AT,
    UPDATED_AT
)
SELECT
    lc.COMMENT_ID,
    lc.POST_ID,
    lc.USER_ID,
    TO_CLOB(lc.CONTENT),
    NVL(lc.CREATED_AT, SYSTIMESTAMP),
    NVL(lc.UPDATED_AT, NVL(lc.CREATED_AT, SYSTIMESTAMP))
FROM COMMENTS_LEGACY lc
WHERE EXISTS (
    SELECT 1
    FROM POSTS p
    WHERE p.POST_ID = lc.POST_ID
)
AND NOT EXISTS (
    SELECT 1
    FROM COMMENTS c
    WHERE c.COMMENT_ID = lc.COMMENT_ID
);

/* 13. Move identity generators past explicit migrated IDs. Requires Oracle 12.2+. */
ALTER TABLE POSTS MODIFY POST_ID GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH LIMIT VALUE);
ALTER TABLE COMMENTS MODIFY COMMENT_ID GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH LIMIT VALUE);

