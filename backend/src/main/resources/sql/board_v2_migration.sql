/*
  Board v2 migration for Oracle.

  Purpose
  - Replace symbol-only POSTS board routing with normalized board structure.
  - Preserve old POSTS / COMMENTS as POSTS_LEGACY / COMMENTS_LEGACY.
  - Migrate legacy post/comment rows into new BOARDS -> POSTS -> COMMENTS model.

  Important
  - Run this after updating backend Board entities/controllers to use BOARD_ID.
  - Current symbol-based Post entity will not match the v2 POSTS table after migration.

  Run once after backing up the database.
*/

/* 1. Logical country market rows for market-level boards. */
MERGE INTO MARKETS m
USING (
    SELECT 'KR' MARKET_CODE, 'Korea Stock Market' MARKET_NAME, 'KOR' COUNTRY, 'KRW' CURRENCY FROM DUAL
    UNION ALL
    SELECT 'US', 'United States Stock Market', 'USA', 'USD' FROM DUAL
) src
ON (m.MARKET_CODE = src.MARKET_CODE)
WHEN NOT MATCHED THEN
    INSERT (MARKET_CODE, MARKET_NAME, COUNTRY, CURRENCY)
    VALUES (src.MARKET_CODE, src.MARKET_NAME, src.COUNTRY, src.CURRENCY);

/* 2. Backup old board tables. Constraint names remain on legacy tables. */
ALTER TABLE COMMENTS RENAME TO COMMENTS_LEGACY;
ALTER TABLE POSTS RENAME TO POSTS_LEGACY;

/* 3. Stock master table. Trading tables can keep SYMBOL columns, but board routing uses STOCK_ID. */
CREATE TABLE STOCKS (
    STOCK_ID    NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    MARKET_CODE VARCHAR2(10)  NOT NULL,
    SYMBOL      VARCHAR2(20)  NOT NULL,
    STOCK_NAME  VARCHAR2(100) NOT NULL,
    CORP_CODE   VARCHAR2(20),
    SECTOR      VARCHAR2(100),
    LISTED      NUMBER(1)     DEFAULT 1 NOT NULL,
    CREATED_AT  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_STOCKS_MARKET_V2
        FOREIGN KEY (MARKET_CODE)
        REFERENCES MARKETS(MARKET_CODE),

    CONSTRAINT UK_STOCKS_MARKET_SYMBOL_V2
        UNIQUE (MARKET_CODE, SYMBOL),

    CONSTRAINT CK_STOCKS_LISTED_V2
        CHECK (LISTED IN (0, 1))
);

CREATE INDEX IDX_STOCKS_SYMBOL_V2 ON STOCKS(SYMBOL);

/* 4. Board classification table. */
CREATE TABLE BOARDS (
    BOARD_ID          NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BOARD_SLUG        VARCHAR2(120) NOT NULL,
    BOARD_NAME        VARCHAR2(100) NOT NULL,
    BOARD_DESCRIPTION VARCHAR2(500),
    BOARD_TYPE        VARCHAR2(30)  NOT NULL,
    MARKET_CODE       VARCHAR2(10),
    STOCK_ID          NUMBER,
    WRITE_ROLE        VARCHAR2(20)  DEFAULT 'USER' NOT NULL,
    HIDDEN            NUMBER(1)     DEFAULT 0 NOT NULL,
    SORT_ORDER        NUMBER        DEFAULT 0 NOT NULL,
    CREATED_AT        TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT UK_BOARDS_SLUG_V2
        UNIQUE (BOARD_SLUG),

    CONSTRAINT FK_BOARDS_MARKET_V2
        FOREIGN KEY (MARKET_CODE)
        REFERENCES MARKETS(MARKET_CODE),

    CONSTRAINT FK_BOARDS_STOCK_V2
        FOREIGN KEY (STOCK_ID)
        REFERENCES STOCKS(STOCK_ID),

    CONSTRAINT CK_BOARDS_TYPE_V2
        CHECK (BOARD_TYPE IN ('GENERAL', 'MARKET', 'STOCK', 'NOTICE')),

    CONSTRAINT CK_BOARDS_WRITE_ROLE_V2
        CHECK (WRITE_ROLE IN ('USER', 'ADMIN')),

    CONSTRAINT CK_BOARDS_HIDDEN_V2
        CHECK (HIDDEN IN (0, 1)),

    CONSTRAINT CK_BOARDS_TARGET_V2
        CHECK (
            (BOARD_TYPE = 'STOCK' AND STOCK_ID IS NOT NULL)
            OR (BOARD_TYPE <> 'STOCK' AND STOCK_ID IS NULL)
        )
);

CREATE INDEX IDX_BOARDS_TYPE_V2 ON BOARDS(BOARD_TYPE);
CREATE INDEX IDX_BOARDS_MARKET_V2 ON BOARDS(MARKET_CODE);
CREATE INDEX IDX_BOARDS_STOCK_V2 ON BOARDS(STOCK_ID);

/* 5. New posts table. BOARD_ID owns post placement. USERNAME and SYMBOL are intentionally removed. */
CREATE TABLE POSTS (
    POST_ID         NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BOARD_ID        NUMBER        NOT NULL,
    USER_ID         NUMBER        NOT NULL,
    TITLE           VARCHAR2(200) NOT NULL,
    CONTENT         CLOB          NOT NULL,
    VIEW_COUNT      NUMBER        DEFAULT 0 NOT NULL,
    RECOMMEND_COUNT NUMBER        DEFAULT 0 NOT NULL,
    CREATED_AT      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_POSTS_BOARD_V2
        FOREIGN KEY (BOARD_ID)
        REFERENCES BOARDS(BOARD_ID),

    CONSTRAINT FK_POSTS_USER_V2
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(ID),

    CONSTRAINT CK_POSTS_COUNTS_V2
        CHECK (VIEW_COUNT >= 0 AND RECOMMEND_COUNT >= 0)
);

CREATE INDEX IDX_POSTS_BOARD_CREATED_V2 ON POSTS(BOARD_ID, CREATED_AT DESC);
CREATE INDEX IDX_POSTS_BOARD_VIEWS_V2 ON POSTS(BOARD_ID, VIEW_COUNT DESC);
CREATE INDEX IDX_POSTS_BOARD_RECOMMENDS_V2 ON POSTS(BOARD_ID, RECOMMEND_COUNT DESC);

/* 6. Recommendation relation. Use this to prevent duplicate recommends per user. */
CREATE TABLE POST_RECOMMENDATIONS (
    POST_ID    NUMBER    NOT NULL,
    USER_ID    NUMBER    NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT PK_POST_RECOMMENDATIONS_V2
        PRIMARY KEY (POST_ID, USER_ID),

    CONSTRAINT FK_POST_RECOMMENDS_POST_V2
        FOREIGN KEY (POST_ID)
        REFERENCES POSTS(POST_ID)
        ON DELETE CASCADE,

    CONSTRAINT FK_POST_RECOMMENDS_USER_V2
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(ID)
);

/* 7. New comments table. USERNAME is resolved through USERS. */
CREATE TABLE COMMENTS (
    COMMENT_ID NUMBER    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    POST_ID    NUMBER    NOT NULL,
    USER_ID    NUMBER    NOT NULL,
    CONTENT    CLOB      NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_COMMENTS_POST_V2
        FOREIGN KEY (POST_ID)
        REFERENCES POSTS(POST_ID)
        ON DELETE CASCADE,

    CONSTRAINT FK_COMMENTS_USER_V2
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(ID)
);

CREATE INDEX IDX_COMMENTS_POST_CREATED_V2 ON COMMENTS(POST_ID, CREATED_AT ASC);

