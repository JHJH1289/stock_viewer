ALTER TABLE TRADE_ORDERS ADD CONSTRAINT FK_ORDERS_STOCK_V2
    FOREIGN KEY (MARKET_CODE, SYMBOL)
    REFERENCES STOCKS(MARKET_CODE, SYMBOL);
*/

