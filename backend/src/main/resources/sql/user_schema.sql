CREATE TABLE USERS (
    ID         NUMBER(19)    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USERNAME   VARCHAR2(50)  NOT NULL,
    EMAIL      VARCHAR2(100) NOT NULL,
    PASSWORD   VARCHAR2(255) NOT NULL,
    ROLE       VARCHAR2(20)  NOT NULL,
    CREATED_AT TIMESTAMP,
    CONSTRAINT UQ_USERS_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE MARKETS (
    MARKET_CODE VARCHAR2(10) NOT NULL,
    MARKET_NAME VARCHAR2(50) NOT NULL,
    COUNTRY     VARCHAR2(3)  NOT NULL,
    CURRENCY    VARCHAR2(3)  NOT NULL,
    CONSTRAINT PK_MARKETS PRIMARY KEY (MARKET_CODE)
);

