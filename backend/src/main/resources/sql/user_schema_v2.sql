CREATE TABLE POSTS (
    POST_ID         NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BOARD_ID        NUMBER        NOT NULL,
    USER_ID         NUMBER        NOT NULL,
    TITLE           VARCHAR2(200) NOT NULL,
    CONTENT         CLOB          NOT NULL,
    VIEW_COUNT      NUMBER        DEFAULT 0 NOT NULL,
    RECOMMEND_COUNT NUMBER        DEFAULT 0 NOT NULL,
    CREATED_AT      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_POSTS_BOARD FOREIGN KEY (BOARD_ID) REFERENCES BOARDS(BOARD_ID),
    CONSTRAINT FK_POSTS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT CK_POSTS_COUNTS CHECK (VIEW_COUNT >= 0 AND RECOMMEND_COUNT >= 0)
);

CREATE INDEX IDX_POSTS_BOARD_CREATED ON POSTS(BOARD_ID, CREATED_AT DESC);
CREATE INDEX IDX_POSTS_BOARD_VIEWS ON POSTS(BOARD_ID, VIEW_COUNT DESC);
CREATE INDEX IDX_POSTS_BOARD_RECOMMENDS ON POSTS(BOARD_ID, RECOMMEND_COUNT DESC);

CREATE TABLE POST_RECOMMENDATIONS (
    POST_ID    NUMBER    NOT NULL,
    USER_ID    NUMBER    NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_POST_RECOMMENDATIONS PRIMARY KEY (POST_ID, USER_ID),
    CONSTRAINT FK_POST_RECOMMENDS_POST FOREIGN KEY (POST_ID) REFERENCES POSTS(POST_ID) ON DELETE CASCADE,
    CONSTRAINT FK_POST_RECOMMENDS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);

CREATE TABLE COMMENTS (
    COMMENT_ID NUMBER    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    POST_ID    NUMBER    NOT NULL,
    USER_ID    NUMBER    NOT NULL,
    CONTENT    CLOB      NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_COMMENTS_POST FOREIGN KEY (POST_ID) REFERENCES POSTS(POST_ID) ON DELETE CASCADE,
    CONSTRAINT FK_COMMENTS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);

CREATE INDEX IDX_COMMENTS_POST_CREATED ON COMMENTS(POST_ID, CREATED_AT ASC);

CREATE TABLE HOLDINGS (
    HOLDING_ID    NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USER_ID       NUMBER        NOT NULL,
    SYMBOL        VARCHAR2(20)  NOT NULL,
    STOCK_NAME    VARCHAR2(100) NOT NULL,
    MARKET_CODE   VARCHAR2(10)  NOT NULL,
    QUANTITY      NUMBER(20)    NOT NULL,
    AVG_BUY_PRICE NUMBER(20,4)  NOT NULL,
    UPDATED_AT    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_HOLDINGS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT FK_HOLDINGS_MARKET FOREIGN KEY (MARKET_CODE) REFERENCES MARKETS(MARKET_CODE),
    CONSTRAINT UQ_HOLDINGS UNIQUE (USER_ID, SYMBOL, MARKET_CODE)
);

CREATE TABLE TRADE_ORDERS (
    ORDER_ID     NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USER_ID      NUMBER        NOT NULL,
    SYMBOL       VARCHAR2(20)  NOT NULL,
    STOCK_NAME   VARCHAR2(100) NOT NULL,
    MARKET_CODE  VARCHAR2(10)  NOT NULL,
    ORDER_TYPE   VARCHAR2(4)   NOT NULL,
    QUANTITY     NUMBER(20)    NOT NULL,
    PRICE        NUMBER(20,4)  NOT NULL,
    TOTAL_AMOUNT NUMBER(20,4)  NOT NULL,
    CURRENCY     VARCHAR2(3)   NOT NULL,
    CREATED_AT   TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_ORDERS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT FK_ORDERS_MARKET FOREIGN KEY (MARKET_CODE) REFERENCES MARKETS(MARKET_CODE),
    CONSTRAINT CHK_ORDER_TYPE CHECK (ORDER_TYPE IN ('BUY', 'SELL'))
);

