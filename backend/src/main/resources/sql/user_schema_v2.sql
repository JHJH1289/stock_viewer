CREATE TABLE BALANCE (
    USER_ID    NUMBER       NOT NULL,
    KRW_AMOUNT NUMBER(20,2) DEFAULT 10000000 NOT NULL,
    USD_AMOUNT NUMBER(20,4) DEFAULT 10000    NOT NULL,
    UPDATED_AT TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_BALANCE PRIMARY KEY (USER_ID),
    CONSTRAINT FK_BALANCE_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);

CREATE TABLE STOCKS (
    STOCK_ID    NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    MARKET_CODE VARCHAR2(10)  NOT NULL,
    SYMBOL      VARCHAR2(20)  NOT NULL,
    STOCK_NAME  VARCHAR2(100) NOT NULL,
    CORP_CODE   VARCHAR2(20),
    SECTOR      VARCHAR2(100),
    LISTED      NUMBER(1)     DEFAULT 1 NOT NULL,
    CREATED_AT  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_STOCKS_MARKET FOREIGN KEY (MARKET_CODE) REFERENCES MARKETS(MARKET_CODE),
    CONSTRAINT UK_STOCKS_MARKET_SYMBOL UNIQUE (MARKET_CODE, SYMBOL),
    CONSTRAINT CK_STOCKS_LISTED CHECK (LISTED IN (0, 1))
);

CREATE INDEX IDX_STOCKS_SYMBOL ON STOCKS(SYMBOL);

CREATE TABLE BOARDS (
    BOARD_ID          NUMBER        GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BOARD_SLUG        VARCHAR2(120) NOT NULL,
    BOARD_NAME        VARCHAR2(100) NOT NULL,
    BOARD_DESCRIPTION VARCHAR2(500),
    BOARD_TYPE        VARCHAR2(30)  NOT NULL,
    MARKET_CODE       VARCHAR2(10),
    STOCK_ID          NUMBER,
    WRITE_ROLE        VARCHAR2(20)  DEFAULT 'USER' NOT NULL,
    HIDDEN            NUMBER(1)     DEFAULT 0 NOT NULL,
    SORT_ORDER        NUMBER        DEFAULT 0 NOT NULL,
    CREATED_AT        TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UK_BOARDS_SLUG UNIQUE (BOARD_SLUG),
    CONSTRAINT FK_BOARDS_MARKET FOREIGN KEY (MARKET_CODE) REFERENCES MARKETS(MARKET_CODE),
    CONSTRAINT FK_BOARDS_STOCK FOREIGN KEY (STOCK_ID) REFERENCES STOCKS(STOCK_ID),
    CONSTRAINT CK_BOARDS_TYPE CHECK (BOARD_TYPE IN ('GENERAL', 'MARKET', 'STOCK', 'NOTICE')),
    CONSTRAINT CK_BOARDS_WRITE_ROLE CHECK (WRITE_ROLE IN ('USER', 'ADMIN')),
    CONSTRAINT CK_BOARDS_HIDDEN CHECK (HIDDEN IN (0, 1)),
    CONSTRAINT CK_BOARDS_TARGET CHECK (
        (BOARD_TYPE = 'STOCK' AND STOCK_ID IS NOT NULL)
        OR (BOARD_TYPE <> 'STOCK' AND STOCK_ID IS NULL)
    )
);

CREATE INDEX IDX_BOARDS_TYPE ON BOARDS(BOARD_TYPE);
CREATE INDEX IDX_BOARDS_MARKET ON BOARDS(MARKET_CODE);
CREATE INDEX IDX_BOARDS_STOCK ON BOARDS(STOCK_ID);

